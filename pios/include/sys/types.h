#ifndef __TYPES_H
#define __TYPES_H

#ifndef _UINT64_T
#define _UINT64_T
typedef unsigned long long  uint64_t;
#endif /* _UINT64_T */

#ifndef _UINT32_T
#define _UINT32_T
typedef unsigned int  uint32_t;
#endif /* _UINT32_T */

#ifndef _UINT16_T
#define _UINT16_T
typedef unsigned short uint16_t;
#endif /* _UINT16_T */

#ifndef _UINT8_T
#define _UINT8_T
typedef unsigned char uint8_t;
#endif /* _UINT8_T */

#ifndef _INT64_T
#define _INT64_T
typedef signed long long  int64_t;
#endif /* _INT64_T */

#ifndef _INT32_T
#define _INT32_T
typedef signed int  int32_t;
#endif /* _INT32_T */

#ifndef _INT16_T
#define _INT16_T
typedef signed short int16_t;
#endif /* _INT16_T */

#ifndef _INT8_T
#define _INT8_T
typedef signed char int8_t;
#endif /* _INT8_T */

#ifndef NULL
#define NULL ((void *)0)
#endif

#endif /* __TYPES_H */
